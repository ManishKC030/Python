# list are container that stores a set of values of any data types.

# list is different than arrays as array stores only of same data types.

List = ['apple','banana', 3, 4.05, 'berry']

print (List)  #output will be ['apple','banana', 3, 4.05, 'berry']

print (List[0]) #output will be apple

#you can also change data in list.

List[0] = 'cherry'

print(List[0]) #output will now be cherry.
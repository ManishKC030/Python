# Text (str)
text_example = "Hello, World!"

# Number (int, float, complex)
int_example = 42
float_example = 3.14
complex_example = 2 + 3j

# Sequence (list, tuple, range)
list_example = [1, 2, 3, "apple"]
tuple_example = (4, 5, 6, "banana")
range_example = range(10)  # Generates numbers from 0 to 9

# Mapping (dict)
dict_example = {
    "name": "Alice", 
    "age": 25, 
    "city": "New York"
}

# Set (set, frozenset)
set_example = {1, 2, 3, 4}
frozenset_example = frozenset([5, 6, 7, 8])

# Boolean (True or False)
bool_example = True # False

# Binary (bytes, bytearray, memoryview)
bytes_example = b"Hello"
bytearray_example = bytearray([65, 66, 67])  # Mutable bytes
memoryview_example = memoryview(bytearray_example)  # View of bytearray

# NoneType (None)
none_example = None
